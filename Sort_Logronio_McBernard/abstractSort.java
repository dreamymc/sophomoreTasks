package Sort_Logronio_McBernard;

abstract class abstractSort {
    public abstract int[] bubbleSort(int[] arr);

    public abstract int[] exchangeSort(int[] arr);

    public abstract int[] selectionSort(int[] arr);

    public abstract int[] insertionSort(int[] array);

    public abstract int[] quickSort(int[] array, int start, int end);

    public abstract int[] mergeSort(int[] arr);

    public abstract int[] bucketSort(int[] arr);
}